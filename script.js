    function applyTranslations(lang) {
      const dict = translations[lang] || translations.fr;
      document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.getAttribute("data-i18n");
        if (dict[key]) {
          el.innerHTML = dict[key];
        }
      });
      document.querySelectorAll(".current-lang-label").forEach((el) => {
        el.textContent = lang.toUpperCase();
      });
    }

    function setTheme(theme) {
      document.documentElement.setAttribute("data-theme", theme);
      ///updateThemeIndicator(theme);
      localStorage.setItem("portfolio-theme", theme);
    }

    // Init on load
    (function init() {
      const savedLang = localStorage.getItem("portfolio-lang") || "fr";
      const savedTheme = localStorage.getItem("portfolio-theme") || "dark";
      setTheme(savedTheme);
      applyTranslations(savedLang);

      document.querySelectorAll(".lang-switch").forEach((btn) => {
        btn.addEventListener("click", () => {
          const lang = btn.getAttribute("data-lang");
          localStorage.setItem("portfolio-lang", lang);
          applyTranslations(lang);
        });
      });

      document.querySelectorAll(".theme-switch").forEach((btn) => {
        btn.addEventListener("click", () => {
          const theme = btn.getAttribute("data-theme-option");
          setTheme(theme);
        });
      });
    })();
// Mettre à jour l'indicateur de thème
function updateThemeIndicator(theme) {
  const themeLabel = document.getElementById('current-theme-label');
  if (themeLabel) {
    themeLabel.textContent = theme.charAt(0).toUpperCase() + theme.slice(1);
  }
}

